for(var j = 0; j<10; j++){
  XX();
  process.stdout.write('\n');
  CC();
  process.stdout.write('\n');
}

function XX(){
  for (var i = 0; i<20; i ++){
    x();
    process.stdout.write('\n');
    c();
    process.stdout.write('\n');
  }
}

function x (){
  process.stdout.write(')(');
}

function c (){
  process.stdout.write('()');
}
